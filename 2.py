def main():
    """
    Программа для подсчета количества общих элементов в двух отсортированных массивах.
    Учитывает кратность вхождений элементов.
    """

    # Считываем размеры массивов
    n1, n2 = map(int, input().split())

    # Считываем первый массив
    A = []
    for _ in range(n1):
        A.append(int(input()))

    # Считываем второй массив
    B = []
    for _ in range(n2):
        B.append(int(input()))

    # Инициализация указателей и счетчика
    i = 0  # указатель для массива A
    j = 0  # указатель для массива B
    count = 0  # счетчик общих элементов

    # Алгоритм двух указателей
    while i < n1 and j < n2:
        if A[i] == B[j]:
            # Найден общий элемент
            count += 1
            i += 1
            j += 1
        elif A[i] < B[j]:
            # Элемент в A меньше, двигаем указатель A
            i += 1
        else:
            # Элемент в B меньше, двигаем указатель B
            j += 1

    # Выводим результат
    print(count)


if __name__ == "__main__":
    main()